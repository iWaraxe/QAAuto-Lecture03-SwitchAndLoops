package com.coherentsolutions.java.webauto.section04;

/**
 * This class demonstrates the basic use of the for loop in Java.
 */
public class ForLoopBasicExample {
    public static void main(String[] args) {
        // For loop to print values of x from 10 to 14
        for (int x = 10; x < 15; x++) {
            System.out.print("Value of x: " + x);
            System.out.print("\n");
        }
    }
}
