package com.coherentsolutions.java.webauto.section04;

/**
 * This class demonstrates a for loop that counts downwards.
 */
public class ForLoopReverse {
    public static void main(String[] args) {
        // For loop to print values of x from 14 to 10
        for (int x = 14; x >= 10; x--) {
            System.out.print("Value of x: " + x);
            System.out.print("\n");
        }
    }
}
